public class Exe2{
    public static void main(String[] args) {
    //USANDO FOR COMO ESTRUTURA DE REPETIÇÃO
    for (int i = 100; i >= 0; i--){
        System.out.println(i);
    }
    //USANDO WHILE COMO ESTRUTURA DE REPETIÇÃO
    int j = 100;
    while(j >= 0) {
      System.out.println(j);
      j--;
    }
    //USANDO DO WHILE COMO ESTRUTURA DE REPETIÇÃO
    int k = 100;
    do{
      System.out.println(k);
      k--;
    }while (k >= 0);
    }
}